class ResNet50Dataset(Dataset):
    """
    Đọc resnet_{split}.jsonl, load ảnh 224×224 JPEG đã resize sẵn.

    Schema JSONL:
        {"resnet_image_path": "train/advertisement/xxx.jpg", "label": "4"}

    __getitem__ trả về:
        {
            "pixel_values": FloatTensor [3, 224, 224]
            "label"        : LongTensor scalar
        }

    Augmentation strategy — ONLINE trong __getitem__:
        Train : TRAIN_TRANSFORM = TRAIN_AUGMENTATIONS + ToTensor + Normalize
        Val/Test: VAL_TRANSFORM  = ToTensor + Normalize

        Tại sao online thay vì offline?
            Mỗi epoch model thấy biến thể ngẫu nhiên khác nhau
            → hiệu quả regularization tốt hơn so với augment 1 lần offline.
            Đây là cách LayoutLMv3 Notebook làm (set_transform online).

        Tham số augmentation = copy 1:1 từ Cell 5 Notebook:
            ColorJitter(brightness=0.3, contrast=0.3, saturation=0.2)
            GaussianBlur(kernel_size=(5,5), sigma=(0.1, 2.0))
            RandomAdjustSharpness(sharpness_factor=2, p=0.5)

    try-except trong __getitem__:
        Tránh crash DataLoader worker khi gặp file JPEG hỏng.
        Trả về tensor zeros thay vì raise exception.
    """

    def __init__(
        self,
        jsonl_path   : str,
        img_base_dir : str = str(_RESNET_IMG_DIR),
        is_train     : bool = False,
    ):
        self.img_base_dir = Path(img_base_dir)
        # Chọn đúng transform dựa trên is_train
        # is_train=True  → TRAIN_TRANSFORM (augment + normalize)
        # is_train=False → VAL_TRANSFORM   (chỉ normalize, không augment)
        self.transform    = TRAIN_TRANSFORM if is_train else VAL_TRANSFORM
        self.samples      = []

        path = Path(jsonl_path)
        if not path.exists():
            raise FileNotFoundError(f"Không tìm thấy: {jsonl_path}")

        with open(path, "r", encoding="utf-8") as f:
            for i, line in enumerate(f):
                line = line.strip()
                if not line:
                    continue
                try:
                    row = json.loads(line)
                    self.samples.append({
                        "img_path": row["resnet_image_path"],
                        "label"   : int(row["label"]),
                    })
                except (json.JSONDecodeError, KeyError, ValueError) as e:
                    log.warning("Dòng %d lỗi, bỏ qua: %s", i, e)
                    continue

        split_type = "train (augmented)" if is_train else "val/test (no aug)"
        log.info("ResNet50Dataset [%s]: %d samples từ %s",
                 split_type, len(self.samples), path.name)

    def __len__(self) -> int:
        return len(self.samples)

    def __getitem__(self, idx: int) -> dict:
        sample   = self.samples[idx]
        img_path = self.img_base_dir / sample["img_path"]

        try:
            # Ảnh đã là 224×224 RGB JPEG từ preprocessing
            # __getitem__ chỉ augment (nếu train) + normalize
            img    = Image.open(img_path).convert("RGB")
            tensor = self.transform(img)

        except Exception as e:
            log.warning("Lỗi load %s: %s — dùng tensor zeros", img_path, e)
            tensor = torch.zeros(3, 224, 224, dtype=torch.float32)

        return {
            "pixel_values": tensor,
            "label"        : torch.tensor(sample["label"], dtype=torch.long),
        }


