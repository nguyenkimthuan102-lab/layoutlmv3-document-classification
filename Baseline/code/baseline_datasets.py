class BERTDataset(Dataset):
    """
    Đọc bert_{split}.jsonl, tokenize bằng BertTokenizer.

    Schema JSONL:
        {"text": "PARLIAMENT INVOICE Date 2024", "label": "11"}

    __getitem__ trả về:
        {
            "input_ids"     : LongTensor [512]
            "attention_mask": LongTensor [512]
            "token_type_ids": LongTensor [512]
            "label"         : LongTensor scalar
        }

    Thiết kế — tokenize trong __getitem__ thay vì __init__:
        32k samples × 512 tokens × 3 arrays ≈ 200MB RAM nếu tokenize hết
        trong __init__. __getitem__ tốn CPU nhưng RAM ổn định.
    """

    def __init__(
        self,
        jsonl_path : str,
        tokenizer  : BertTokenizer,
        max_length : int = 512,
    ):
        self.tokenizer  = tokenizer
        self.max_length = max_length
        self.samples    = []

        path = Path(jsonl_path)
        if not path.exists():
            raise FileNotFoundError(f"Không tìm thấy: {jsonl_path}")

        with open(path, "r", encoding="utf-8") as f:
            for i, line in enumerate(f):
                line = line.strip()
                if not line:
                    continue
                try:
                    row = json.loads(line)
                    self.samples.append({
                        "text" : row["text"],
                        "label": int(row["label"]),
                    })
                except (json.JSONDecodeError, KeyError, ValueError) as e:
                    log.warning("Dòng %d lỗi, bỏ qua: %s", i, e)
                    continue

        log.info("BERTDataset: %d samples từ %s", len(self.samples), path.name)

    def __len__(self) -> int:
        return len(self.samples)

    def __getitem__(self, idx: int) -> dict:
        sample = self.samples[idx]

        encoding = self.tokenizer(
            sample["text"],
            padding        = "max_length",
            truncation     = True,
            max_length     = self.max_length,
            return_tensors = "pt",
        )

        return {
            # squeeze(0): bỏ batch dim do return_tensors="pt" thêm vào
            "input_ids"     : encoding["input_ids"].squeeze(0),
            "attention_mask": encoding["attention_mask"].squeeze(0),
            "token_type_ids": encoding["token_type_ids"].squeeze(0),
            "label"         : torch.tensor(sample["label"], dtype=torch.long),
        }


