# Val/Test transform: chỉ ToTensor + ImageNet Normalize, không augment
VAL_TRANSFORM = Compose([
    ToTensor(),
    Normalize(mean=[0.485, 0.456, 0.406],
              std=[0.229, 0.224, 0.225]),
])

# Train transform: augment trước rồi mới ToTensor + Normalize
TRAIN_TRANSFORM = Compose([
    TRAIN_AUGMENTATIONS,   # pixel-level, không thay đổi tọa độ
    ToTensor(),
    Normalize(mean=[0.485, 0.456, 0.406],
              std=[0.229, 0.224, 0.225]),
])


