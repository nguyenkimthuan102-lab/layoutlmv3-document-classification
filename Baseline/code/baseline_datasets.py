def make_bert_loaders(
    data_dir   : str = str(_DRIVE_DIR),
    batch_size : int = 16,
    max_length : int = 512,
) -> dict[str, DataLoader]:
    """
    Tạo DataLoader cho cả 3 splits của BERT.

    num_workers=4  : 4 process song song load + tokenize
    pin_memory=True: tensor copy thẳng vào CUDA pinned memory
                     → tăng tốc transfer CPU→GPU ~20%
    shuffle=True chỉ cho train.

    Returns: {"train": DataLoader, "val": DataLoader, "test": DataLoader}
    """
    tokenizer = BertTokenizer.from_pretrained("bert-base-uncased")
    loaders   = {}

    for split in ["train", "val", "test"]:
        jsonl = Path(data_dir) / f"bert_{split}.jsonl"
        ds    = BERTDataset(str(jsonl), tokenizer, max_length=max_length)

        loaders[split] = DataLoader(
            ds,
            batch_size  = batch_size,
            shuffle     = (split == "train"),
            num_workers = 4,
            pin_memory  = True,
        )

    return loaders


def make_resnet_loaders(
    data_dir     : str = str(_DRIVE_DIR),
    img_base_dir : str = str(_RESNET_IMG_DIR),
    batch_size   : int = 32,
) -> dict[str, DataLoader]:
    """
    Tạo DataLoader cho cả 3 splits của ResNet50.

    Train set  : is_train=True  → TRAIN_TRANSFORM (augment online)
    Val/Test   : is_train=False → VAL_TRANSFORM   (không augment)

    Augmentation bốc nguyên xi từ Cell 5 Notebook — không thay đổi tham số.

    Returns: {"train": DataLoader, "val": DataLoader, "test": DataLoader}
    """
    loaders = {}

    for split in ["train", "val", "test"]:
        jsonl = Path(data_dir) / f"resnet_{split}.jsonl"
        ds    = ResNet50Dataset(
            str(jsonl),
            img_base_dir = img_base_dir,
            is_train     = (split == "train"),
        )

        loaders[split] = DataLoader(
            ds,
            batch_size  = batch_size,
            shuffle     = (split == "train"),
            num_workers = 4,
            pin_memory  = True,
        )

    return loaders


